#!/bin/bash
echo "--------------------------------------------------------------------------------------------"
echo "Packing application"
echo "--------------------------------------------------------------------------------------------"
mvn clean package

echo "--------------------------------------------------------------------------------------------"
echo "Building image"
echo "--------------------------------------------------------------------------------------------"
docker build -f src/main/docker/Dockerfile.jvm -t quarkus-jvm .

echo "--------------------------------------------------------------------------------------------"
echo "Running image"
echo "--------------------------------------------------------------------------------------------"
docker run -i --rm --net=host quarkus-jvm